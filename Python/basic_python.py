
# مبانی پایتون

# معرفی پایتون
# پایتون یک زبان برنامه‌نویسی سطح بالا و همه‌منظوره است که برای کاربردهای مختلفی از جمله مهندسی داده، توسعه وب و هوش مصنوعی استفاده می‌شود.

# متغیرها و انواع داده‌ها
اسم = "علی"
سن = 25
وزن = 70.5
متاهل = False

# چاپ متغیرها
print(f"اسم: {اسم}")
print(f"سن: {سن}")
print(f"وزن: {وزن}")
print(f"متاهل: {متاهل}")

# عملگرها
جمع = 10 + 5
تفریق = 10 - 5
ضرب = 10 * 5
تقسیم = 10 / 5

print(f"جمع: {جمع}, تفریق: {تفریق}, ضرب: {ضرب}, تقسیم: {تقسیم}")

# عملگرهای منطقی
x = 10
y = 5
print(x > y)  # True
print(x < y)  # False

# شرط‌ها
سن = 20
if سن >= 18:
    print("بزرگسال")
else:
    print("کودک")

# حلقه‌ها
for i in range(5):
    print(i)

i = 0
while i < 5:
    print(i)
    i += 1

# توابع
def جمع_اعداد(a, b):
    return a + b

نتیجه = جمع_اعداد(5, 10)
print(f"نتیجه جمع: {نتیجه}")
